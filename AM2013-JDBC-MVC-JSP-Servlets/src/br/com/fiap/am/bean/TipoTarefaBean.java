package br.com.fiap.am.bean;

public class TipoTarefaBean {
	
	private int cdTipoTarefa;
	private String descricao;
	
	public TipoTarefaBean() {}
	
	public int getCdTipoTarefa() {
		return cdTipoTarefa;
	}
	
	public void setCdTipoTarefa(int cdTipoTarefa) {
		this.cdTipoTarefa = cdTipoTarefa;
	}
	
	public String getDescricao() {
		return descricao;
	}
	
	public void setDescricao(String descricao) {
		this.descricao = descricao;
	}
	

}
